#include <iostream>
using namespace std;

int main() {
    int a, b, c, ans=0; // a:고정비용, b:가변비용, c:판매비용
    cin >> a >> b >> c;
    if(b>=c) ans=-1; // 손익분기점이 존재하지 않음
    else ans=a/(c-b)+1;
    cout << ans << '\n';
}

